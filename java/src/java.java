public class java {
    /*
     //주석
     */
    //main()메서드가 필요하며 모든 Java 프로그램에서 이를 확인할수 있다
    //내무 모든 코드가 main()에 실행됩니다
    //class파일 이름과 일치해야 하며 모든 프로그램에는 다음이 포홤되어야합니다 main()방법

    public static void main(String[] args) {
        System.out.println("hello javas 1");
        System.out.println("java 2");
        //코드로 작성된 Hello World 메세지 포함
        //자바 구문
        System.out.println();
        //println() 화면에 텍스트 한줄을 인쇄하는 방법
        //{}코드 블록의 시작과 끝을 표시합니다
        //System 내장 java클래스
        //out 출력의 줄임말
        //println()메서드 인쇄선을 줄임말
        System.out.println("Hello World");
        //출력/인쇄
        //텍스트인쇄
        System.out.println("Hello world");
        //새줄 추가
        System.out.println("It is awesome!");
        //이중 따옴표
        System.out.println("This is Java");
        //인쇄방법
        //print() - 새줄을 삽입하지 않는다
        System.out.print("I will do this Java");
        System.out.print("that do that");
        //안쇄번호
        //큰따옴표에 숫자 넣지 않는다
        System.out.println(300);
        //수학계산
        System.out.println(5+8);
        //주석
        /*
        다중줄 주석
         */
    }
}
